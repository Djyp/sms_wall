<h2>Il faut avoir :</h2>
	<ul>
		<li>Un ordinateur (avec Java à jour) et une connexion internet</li>
		<li>Un téléphone (avec Android 2.2 ou +) connecté en wifi ou en 3G</li>
		<li>Un vidéoprojecteur</li>
		<li>Un mail (dédié pour les sondages) sous gmail</li>
	</ul>

<h2>Installation : appli sur son téléphone</h2>
	<ul>
		<li>Installer l'appli "SMS to Email" : <a href="https://www.smstomail.nl/beta/" title="SMS to Email" target="_blank">https://www.smstomail.nl/beta/</a></li>
		<li>Configurer "SMS to Email" en entrant votre adresse gmail de sondages</li>
	</ul>

<h2>Installation : logiciel sur ordinateur</h2>
	<ul>
		<li>Télécharger, décompresser puis lancer le logiciel votesms (3,9 Mo) : <a href="http://demo.lespetitsdebrouillardspc.org/sondages/votesms_v1.zip" title="Votes SMS">http://demo.lespetitsdebrouillardspc.org/sondages/votesms_v1.zip</a></li>
		<li>
		<ul>Dans l'onglet configuration : 
			<li>renseigner les champs sondage (inutile de remplir les 4, vous pouvez faire simplement un OUI/NON)</li>
			<li>renseigner le titre du sondage</li>
			<li>renseigner l'adresse mail et le mot de passe (adresse sondages gmail)
				<br/><i>NB : désolé pas réussi à cacher le MDP</i>
			</li>
		</ul>
		</li>
		<li>Cliquer sur l'onglet demarrer
	</ul>

<h2>
Tous les SMS qui passeront sur le téléphone, 
<br/>avec comme texte un des champs remplis, 
<br/>seront renvoyés au logiciel et affichés sur le mur.
</h2>


